#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*ret;

	i = 0;
	if (!str)
		return (NULL);
	if (!(ret = (char *)malloc(sizeof(char) * (ft_strlen(str) + 1))))
		return (NULL);
	while (str[i])
	{
		ret[i] = str[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
