#include "libft.h"

char	*ft_strnstr(char *str, char *to_find, int size)
{
	int i;
	int j;

	i = 0;
	if (*to_find == '\0')
		return (str);
	while (i < size && str[i] != '\0')
	{
		j = 0;
		while (str[i + j] == to_find[j] && to_find[j] != '\0')
			j++;
		if (to_find[j] == '\0')
			return (str + i);
		i++;
	}
	return (NULL);
}
